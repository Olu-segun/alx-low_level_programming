#include "main.h"
/**
 * main: Entry Point.
 *
 * print_alphabet_x10: A function that prints 10 times the alphabet.
 *
 * Return: Always 0 (Success)
 */

void print_alphabet_x10(void)
{
	int i, j;

		for (i = 0; i <= 9; i++)
		{
			for (j = 67; j <= 122; j++)
			{
				_putchar('j');
			}
	_putchar('\n');
		}
}
