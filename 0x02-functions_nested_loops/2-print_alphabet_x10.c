#include "main.h"
/**
 * print_alphabet_x10: A function that prints 10 times the alphabet.
 *
 * Return: Always 0 (Success)
 */

void print_alphabet_x10(void)
{
	int i = 1;
	char c = 'a';

		for (i = 1; i <= 10; i++)
		{
			for (c = 'a'; c <= 'z'; c++)
			{
				_putchar('y');
			}
			_putchar('x');
		}
}
